from numpy import sign

class Knot:
    def __init__(self, head = False):
        self.pos = [11,15]
        self.direction = [0,0]
        self.head = head
        
    def move(self, target):
        if not self.head:
            move = any(abs(self.pos[i] - target[i]) > 1 for i in range(2))
            for i in range(2):
                self.direction[i] = sign(target[i] - self.pos[i])
                if move:
                    self.pos[i] += self.direction[i]
        else:
            for i in range(2):
                self.pos[i] += target[i]

def get_instruction(i):
    listkey = {"U": [0,-1], "R": [1, 0], "D": [0, 1], "L": [-1, 0]}
    target, mult = i.split()
    return listkey[target], int(mult)


#read file
with open("input.txt") as f:
    instructions = f.read().split("\n")[:-1]

#build rope
rope = [Knot(True)]
for _ in range(9):
    rope.append(Knot())
tail_locations = set()

#move
for cmd in instructions:
    target, mult = get_instruction(cmd)

    for _ in range(mult):
        for i, knot in enumerate(rope):
            if knot.head:
                knot.move(target)
            else:
                knot.move(rope[i-1].pos)
        tail_locations.add((*rope[-1].pos,))
        
print(len(tail_locations))

